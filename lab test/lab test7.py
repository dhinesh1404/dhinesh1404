#Receive two integer numbers from the user.
num1 = int(input("Enter the first number :"))
num2 = int(input("Enter the second number :"))

#Calculate the sum, difference, multiplication, and division results.
total = num1 + num2
difference = num1 - num2
product = num1 * num2
division = num1 / num2

#Print the calculate result.
print("Total:", float(total))
print("Difference:", float(difference))
print("Product:", float(product))
print("Division:", float(division))