#Get an integer and output "special" if it's even. "Not ouput anything if it's not even."
input_value = int(input("정수 입력: ")) 

if input_value % 2 == 0:
    print("even")
elif input_value % 2 == 1:
    print("odd")
    
if input_value % 2 == 0:
    print("even")
elif input_value % 2 == 1:
    print("odd")
    
if input_value % 2 == 0:
    print("even")
else:
    print()